            <div class="sharethis-inline-share-buttons"></div> <br>
                <h5 style="text-align:center; font-size: 1.3em">Chalo Holds Annual Talent Show</h5><br>
                <div class="newsDateandAuthor" style="margin-bottom:5px;"><i class="fa fa-calendar" aria-hidden="true"></i>
                    16th July , 2017 &nbsp; <i class="fa fa-user" aria-hidden="true"></i>
                    By Lombe
                </div>
            
                <p>The parents, students, and staff of Chalo Trust School were treated to the annual talent show on the 15th of July, 2017. The show, which has been held every year since the inception of the school, was our 14th Talent Show. The performances from our students were beyond incredible and there was a wonderful display of the many talents our students have. There was singing, dancing, acrobatics, instrument playing, poetry, drawing, baking and several other talents on display in an action packed show.</p>
            
                <p>Jenny Chileshe, school director, said the talent show has been a long standing tradition for the school.

                    “The school has been putting this on forever,” she said. “I have been a part of the talent show for 10-plus years.”</p>

                     <p>The students must first audition in front of teachers and also have a rehearsal day to practice their act before they face the rest of the students. The show featured a variety of acts including multiple singing and dance acts, comedy skits and even synchronized acrobats.

                    “It’s something to entertain the kids and it’s just really fun,” she said. “It’s a place where our pupils can showcase their God-given talents. They look forward to this from the first term and some of them start practicing as early as the first term, and we always tell them to think of new or unusual acts to make the show better.”</p>
                <p>
                    
                Every year the school holds the talent show in the second term and this gives all of our students, both primary and secondary, a chance to showcase their abilities. During the talent show, the students get to see the amazing, and sometimes surprising skills that their classmates possess.
               </p>


