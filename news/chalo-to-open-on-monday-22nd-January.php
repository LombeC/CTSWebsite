                 <p>Chalo Trust School will open on Monday 22nd January 2018. Schools were initially slated to be opened on the 2nd of January, but a cholera outbreak meant that all schools in the country had to push back their intended opening dates. The Zambian government announced that schools would be allowed to re-open pending a health inspection. We underwent the inspection and the Ministry of Health has granted us authorization to open the school</p>
            
                 <p>We expect all our students to be in school by Monday as we intend to begin classes on Tuesday 23rd January. Our School Calendar has been adjusted to make sure that we cover the full 13 weeks. In that regard, the closing dates for term 1 have been pushed to the 19th of April. The April holiday will also be slightly shortened to ensure our students cover all the work stipulated by our curriculum. Vist our <a href="../calendar.php">Calendar</a> to see all the events we have lined up for term 1.</p>
            
                <p> Despite the disruption, we look forward to having a productive term! On behalf of the school management, we welcome all you to 2018 term 1. For our potential new parents, enrollment for all grades from Baby Class to Grade 12 is still going on and we would encourage you to come and visit us as soon as possible  </p> 
            
            
                
