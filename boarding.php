                <h6>Tuck-Shop</h6>
                The school runs a Tuck shop that supplies pupils and the surrounding community with essentials. Pupils are not, however, allowed to keep pocket money on their person. Instead, parents are advised to deposit a sum of  money stipulated by school authorities from time to time. Against this amount, pupils get provisions from the tuck-shop. In order to help them budget, only a limited amount is allowed per week and children who exhaust this allocation, are not allowed to buy for that particular week. The school also provides a snack at break.
                
                <h6>School Clinic</h6>
                The School has a well stocked clinic to provide primary health care to all our students and staff. The clinic is run by a registered nurse who is always on call and is always ready to provide health care to our students. As we have a small community, we only provide primary healthcare. So if students suffer from any major illnesses or require surgery on anything more complicated, we immediately call the parents to come in and pick up the students or in the case of emergencies, we provide a vehicle to transfer the pupil to the nearest hospital.
