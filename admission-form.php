                <h3 style="color:#023681; text-transform:uppercase;">A Detailed Look at the&nbsp;Admission Process</h3>
                    <strong>Inquiry</strong><br />
                    When a family contacts the Office of Admission via telephone, email or in person, this is considered an inquiry, which is the first step in the admission process. Inquiries are received throughout the year and are welcome anytime. Some families contact the school two years before they might consider applying, which is perfectly acceptable as well. An inquiry may be as simple as a one question exchange, or an in-depth conversation and is not a commitment or obligation to apply.<br />
                    <br />
                    <strong>Application Process</strong><br />
                    The application process is multi-tiered and includes submitting a formal application, as well as in-person or telephone interviews with the School Principal. Families learn about the school’s mission and values, programs, facilities, and community. At the same time, the school learns about a family’s reasons for exploring Chalo Trust School and the goals they have for their child’s education. A tour of the school typically takes place at this point.<br />
                    <br />
                
                    A complete application for admission consists of:
                    <ul>
                        <li>Completed application form. </li>
                        <li>1 year of school records including grades and formal transcripts.</li>
                        <li>For students applying to Grade 8 or Grade 10, their records of the Zambian National Examinations will be required.</li>
                        <li>In cases where students may not have access to their old records, a simple test might be rendered to assess the childs basic of understanding of literacy and math.</li>
                        <li>Medical and Background Information</li>
                    </ul>
                    <strong>Application Review</strong><br />
                    Each application is reviewed by a committee that is led by the Principal and is comprised of staff and administrators. They consider school records, relevant testing data, personal and family interviews, visit day observations, and other aspects of the applicant’s learning profile to determine:
                    <ul>
                        <li>Is the applicant ready for us, and are we ready for them? &nbsp;</li>
                        <li>Are they capable of being successful in our learning environment; can we challenge and support them, recognizing their needs as an individual learner?</li>
                        <li>Does the applicant demonstrate strong character and the potential for leadership?</li>
                        <li>Will the family partner with the school in supporting the needs of the student?</li>
                    </ul>
                    <strong>Notification of Decision</strong><br />
                    Applicants are notified by the School Principal one of the following outcomes: 
                    <ol>
                        <li>Accepted and offered enrollment</li>
                        <li>Denied admission if the admission committee determines that an applicant is not a good fit for the school.</li>
                    </ol>
