<h6>TRAINING IN MORALITY </h6>

All pupils are exposed to a compulsory course in moral issues and 
cultural aspects of the Zambian situation. In this course pupils 
discover about acceptable conduct in their daily life both in and 
outside school; they learn about respect for elders, respect for 
other people and responsibility roles in different settings including 
home, work, school , church and among peers. <br>
        
        <h6>CLASS SIZE</h6>
There are three blocks, each with four large ultra-modern classrooms. The blocks are connected by a concrete walk- way  in order to make movement from one block to another easy during the rain season. In order to provide maximum contact and help from teachers, the class size is restricted to 30 pupils at all levels.
        
